    INTEGER,POINTER :: NCLN
    INTEGER,POINTER :: ICLNCB
    INTEGER,POINTER :: ICLNHD
    INTEGER,POINTER :: ICLNDD
    INTEGER,POINTER :: ICLNIB
    INTEGER,POINTER :: NCLNNDS
    INTEGER,POINTER :: NCLNGWC
    INTEGER,POINTER :: NCONDUITYP
    INTEGER,DIMENSION(:),POINTER :: NNDCLN
    INTEGER,DIMENSION(:),POINTER :: IFLINCLN
    INTEGER,DIMENSION(:),POINTER :: ICCWADICLN
    INTEGER,DIMENSION(:),POINTER :: ICGWADICLN
    INTEGER,DIMENSION(:),POINTER :: CLNCON
    REAL,DIMENSION(:,:),POINTER :: ACLNNDS
    REAL,DIMENSION(:,:),POINTER :: ACLNGWC
    REAL,DIMENSION(:,:),POINTER :: ACLNCOND
    DOUBLE PRECISION,POINTER,DIMENSION(:) :: HWADICC
    DOUBLE PRECISION,POINTER,DIMENSION(:) :: HWADICG
    DOUBLE PRECISION,POINTER,DIMENSION(:) :: DWADICC
    DOUBLE PRECISION,POINTER,DIMENSION(:) :: DWADICG
