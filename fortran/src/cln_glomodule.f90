  INTEGER,SAVE,POINTER :: NCLNGWC
  INTEGER,SAVE,POINTER :: NCONDUITYP
  INTEGER,SAVE,DIMENSION(:),POINTER :: NNDCLN
  INTEGER,SAVE,DIMENSION(:),POINTER :: IFLINCLN
  INTEGER,SAVE,DIMENSION(:),POINTER :: ICCWADICLN
  INTEGER,SAVE,DIMENSION(:),POINTER :: ICGWADICLN
  INTEGER,SAVE,DIMENSION(:),POINTER :: CLNCON
  REAL,SAVE,DIMENSION(:,:),POINTER :: ACLNNDS
  REAL,SAVE,DIMENSION(:,:),POINTER :: ACLNGWC
  REAL,SAVE,DIMENSION(:,:),POINTER :: ACLNCOND
  DOUBLE PRECISION,SAVE,POINTER,DIMENSION(:) :: HWADICC
  DOUBLE PRECISION,SAVE,POINTER,DIMENSION(:) :: HWADICG
  DOUBLE PRECISION,SAVE,POINTER,DIMENSION(:) :: DWADICC
  DOUBLE PRECISION,SAVE,POINTER,DIMENSION(:) :: DWADICG
