    IMPLICIT NONE
    CLASS(CLNGLOTYPE),INTENT(INOUT) :: THIS
    DEALLOCATE(THIS%NCLN)
    DEALLOCATE(THIS%ICLNCB)
    DEALLOCATE(THIS%ICLNHD)
    DEALLOCATE(THIS%ICLNDD)
    DEALLOCATE(THIS%ICLNIB)
    DEALLOCATE(THIS%NCLNNDS)
    DEALLOCATE(THIS%NCLNGWC)
    DEALLOCATE(THIS%NCONDUITYP)
    DEALLOCATE(THIS%NNDCLN)
    DEALLOCATE(THIS%IFLINCLN)
    DEALLOCATE(THIS%ICCWADICLN)
    DEALLOCATE(THIS%ICGWADICLN)
    DEALLOCATE(THIS%CLNCON)
    DEALLOCATE(THIS%ACLNNDS)
    DEALLOCATE(THIS%ACLNGWC)
    DEALLOCATE(THIS%ACLNCOND)
    DEALLOCATE(THIS%HWADICC)
    DEALLOCATE(THIS%HWADICG)
    DEALLOCATE(THIS%DWADICC)
    DEALLOCATE(THIS%DWADICG)
  END SUBROUTINE DESTROY
  SUBROUTINE PNTSAV(THIS)
    IMPLICIT NONE
    CLASS(CLNGLOTYPE),INTENT(INOUT) :: THIS
    THIS%NCLN=>NCLN
    THIS%ICLNCB=>ICLNCB
    THIS%ICLNHD=>ICLNHD
    THIS%ICLNDD=>ICLNDD
    THIS%ICLNIB=>ICLNIB
    THIS%NCLNNDS=>NCLNNDS
    THIS%NCLNGWC=>NCLNGWC
    THIS%NCONDUITYP=>NCONDUITYP
    THIS%NNDCLN=>NNDCLN
    THIS%IFLINCLN=>IFLINCLN
    THIS%ICCWADICLN=>ICCWADICLN
    THIS%ICGWADICLN=>ICGWADICLN
    THIS%CLNCON=>CLNCON
    THIS%ACLNNDS=>ACLNNDS
    THIS%ACLNGWC=>ACLNGWC
    THIS%ACLNCOND=>ACLNCOND
    THIS%HWADICC=>HWADICC
    THIS%HWADICG=>HWADICG
    THIS%DWADICC=>DWADICC
    THIS%DWADICG=>DWADICG
  END SUBROUTINE PNTSAV
  SUBROUTINE PNTSET(THIS)
    IMPLICIT NONE
    CLASS(CLNGLOTYPE),INTENT(INOUT) :: THIS
    NCLN=>THIS%NCLN
    ICLNCB=>THIS%ICLNCB
    ICLNHD=>THIS%ICLNHD
    ICLNDD=>THIS%ICLNDD
    ICLNIB=>THIS%ICLNIB
    NCLNNDS=>THIS%NCLNNDS
    NCLNGWC=>THIS%NCLNGWC
    NCONDUITYP=>THIS%NCONDUITYP
