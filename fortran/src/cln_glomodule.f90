MODULE CLN1MODULE
  IMPLICIT NONE
  INTEGER,SAVE,POINTER :: NCLN
  INTEGER,SAVE,POINTER :: ICLNCB
  INTEGER,SAVE,POINTER :: ICLNHD
  INTEGER,SAVE,POINTER :: ICLNDD
  INTEGER,SAVE,POINTER :: ICLNIB
