    ICGWADICLN=>THIS%ICGWADICLN
    CLNCON=>THIS%CLNCON
    ACLNNDS=>THIS%ACLNNDS
    ACLNGWC=>THIS%ACLNGWC
    ACLNCOND=>THIS%ACLNCOND
    HWADICC=>THIS%HWADICC
    HWADICG=>THIS%HWADICG
    DWADICC=>THIS%DWADICC
    DWADICG=>THIS%DWADICG
  END SUBROUTINE PNTSET
END MODULE CLN1MODULE
